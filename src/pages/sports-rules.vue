      <p class="rules-page__paragraph">Regla Dead Heat</p>
      <p class="rules-page__paragraph">Para las apuestas de golf que contiene más de dos participantes, tales como 3
        Bolas, 6 Shooter Ball mejores
        jugadores en un grupo y el líder de Ronda (S), por ejemplo, que dos o más jugadores empatan en el mismo lugar se
        aplica la regla de empate de la siguiente manera: La apuesta se reduce de acuerdo con la cantidad de jugadores
        que empatan y luego se aplica a las cuotas completas con el resto de la apuesta considerada como perdida.</p>
      <p class="rules-page__paragraph">Por ejemplo:</p>
      <p class="rules-page__paragraph">Apuesta $ 150 con una probabilidad de +200 y 2 jugadores empatan en la misma
        posición. Se pierde la mitad de la
        apuesta: $ 75, la otra mitad gana y se le pagan las mismas probabilidades. Entonces $ 75 gana a +200 = $ 150 – $
        75 = $ 75 netos.</p>
      <p class="rules-page__paragraph">Si 3 jugadores empatan en la misma posición, entonces: dos tercios de la apuesta se
        pierden – $ 100, los
        terceros $50 restantes ganan a +200.</p>
      <p class="rules-page__paragraph">Tenga en cuenta que esta regla no se aplica a los enfrentamientos de dos jugadores
        donde un empate resulta en
        un empate.</p>
