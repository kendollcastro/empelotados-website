        // Si la sección seleccionada ya está abierta, simplemente la cerramos.
        state.activeSections.splice(currentIndex, 1);
      }
    };

    const isActive = (index) => {
      return state.activeSections.includes(index);
    };

    return {
      toggleSection,
      isActive,
    };
